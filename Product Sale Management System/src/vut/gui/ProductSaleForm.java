    private void btnSaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaleActionPerformed
        try {
            if (radIBP.isSelected()) {
                objProd = new ItemBasedProduct(txtBarcode.getText(), txtName.getText(), ProductCategory.IBP,
                        Integer.parseInt(txtUnitPrice.getText()), txtManufacturer.getText());
                objProd.addProduct(objProd);
            } else {
                objProd = new WeightBasedProduct(txtBarcode.getText(), txtName.getText(), ProductCategory.WBP,
                        Integer.parseInt(txtUnitPrice.getText()), Integer.parseInt(txtProdWeight.getText()));
                objProd.addProduct(objProd);
            }
            clear();
        } catch (InputMismatchException ex) {
            JOptionPane.showMessageDialog(this, "ERROR! Invalid numeric value\n" + ex.getMessage());
        } catch (DuplicateException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaleActionPerformed

