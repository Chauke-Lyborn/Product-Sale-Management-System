    private void btnViewIBPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewIBPActionPerformed

        taDisplay.setText("List of item based products Sold\n\n");
        ArrayList<Product> arListProduct = new ArrayList<>();
        try {
            arListProduct = objProd.returnAll();
        } catch (NotFoundException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

        for (int i = 0; i < arListProduct.size(); i++) {
            if (arListProduct.get(i).getProductCategory().equals(ProductCategory.IBP)) {
                taDisplay.append(arListProduct.get(i).toString());
            }
        }

    }//GEN-LAST:event_btnViewIBPActionPerformed

    private void btnUpdatePriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePriceActionPerformed
        String barcodeToUpdate;
        int percent;
        barcodeToUpdate = JOptionPane.showInputDialog(this, "Enter the Barcode of the product you want to update Price:");
        percent = Integer.parseInt(JOptionPane.showInputDialog(this, "Enter the Percent to increse the Price"));

        try {
            objProd.updatePrice(barcodeToUpdate, percent);

        } catch (NotFoundException ex) {
            JOptionPane.showConfirmDialog(this, ex.getMessage());
        } catch (InputMismatchException ex) {
            JOptionPane.showMessageDialog(this, "Increase Percentage must be an INTEGER", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdatePriceActionPerformed

